<template>
  <div>
    <h1>Home</h1>
    <scale
      v-for="(example, index) in examples"
      :key="example.type"
      :from="dimensions[index].from"
      :to="dimensions[index].to"
      :progress="progress"
    >
      <screen :type="example.type">
        <wallpaper :options="example.options" :width="dimensions[index].from.width" :height="dimensions[index].from.height"/>
      </screen>
    </scale>
  </div>
</template>

<script>
import Wallpaper from "@/components/Wallpaper";
import Scale from "@/components/Scale";
import Screen from "@/components/Screen";
import { useWindowScroll } from "@/mixins/useWindowScroll";
import { useWindowSize } from "@/mixins/useWindowSize";
import { map } from "@/utils/math";
import fontURL from "@/assets/font/LuckiestGuy.woff2";
const [MIN_Y, MAX_Y] = [0, 200];
export default {
  name: "Home",
  mixins: [useWindowScroll(MIN_Y, MAX_Y), useWindowSize()],
  data() {
    return {
      examples: [
        {
          type: "mac",
          options: {
            title: "How are you?",
            bgColor: "#132743",
            textColor: "#d7385e",
            fontSize: 130,
            fontFamily: "Luckiest Guy",
            fontURL,
          },
        },
      ],
    };
  },
  components: {
    Wallpaper,
    Scale,
    Screen,
  },
  computed: {
    progress: function () {
      return map(this.scrollY, MIN_Y, MAX_Y, 0, 1);
    },
    dimensions: function () {
      const scale = 0.5;
      return [
        {
          from: {
            x: 0,
            y: 0,
            width: this.windowWidth,
            height: this.windowHeight,
          },
          to: {
            width: this.windowWidth * scale,
            height: this.windowHeight * scale,
            x: (this.windowWidth * (1 - scale)) / 2,
            y: this.windowHeight - 100 - this.windowHeight * scale,
          },
        },
      ];
    },
  },
};
</script>

